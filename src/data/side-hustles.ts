export function getHustleBySlug(slug: string): SideHustle | undefined {
  return sideHustles.find((h) => h.slug === slug);
}

export function getHustlesByCategory(category: string): SideHustle[] {
  return sideHustles.filter((h) => h.category === category);
}

export function getTrendingHustles(): SideHustle[] {
  return sideHustles.filter((h) => h.is_trending).sort((a, b) => b.popularity_score - a.popularity_score);
}

export function getTopHustles(count: number = 6): SideHustle[] {
  return [...sideHustles].sort((a, b) => b.popularity_score - a.popularity_score).slice(0, count);
}

export function searchHustles(query: string): SideHustle[] {
  const q = query.toLowerCase();
  return sideHustles.filter(
    (h) =>
      h.name.toLowerCase().includes(q) ||
      h.description.toLowerCase().includes(q) ||
      h.category.toLowerCase().includes(q) ||
      h.skills_required.some((s) => s.toLowerCase().includes(q))
  );
}
